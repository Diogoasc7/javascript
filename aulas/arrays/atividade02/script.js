/* Iteração com o For...In:
Crie um array de objetos representando produtos. Use um loop for...in para iterar sobre o array e imprimir o nome de cada produto
*/

let produtos = ['camisa, meiao, short, agasalho, chuteira']
for(let indice in produtos){
    console.log(`Os objetos dentro do array são ${produtos[indice]}`)
}